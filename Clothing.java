public class Clothing extends Product
{
  private String brand;
  private String size;

  public String getBrand()
  {
    return brand;
  }

  public String getSize()
  {
    return size;
  }

  public void setBrand(String b)
  {
    brand = b;
  }

  public void setSize(String s)
  {
    size = s;
  }
  
}